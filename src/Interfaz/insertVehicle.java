    private void btn_insertVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertVehicleActionPerformed
        // TODO add your handling code here:
        Boolean selected=null;
        try{
         try{
             //creando el  archivo
             
            File file= new File("./vehicle.dat");
            VehicleFile vehicleFile = new VehicleFile(file);
            
            //captura los datos del tex field
            
            String nameV= tfd_name.getText();
            int yearV= Integer.parseInt(tfd_year.getText());
            float mileageV= Float.parseFloat(tfd_mileage.getText());
            if((cb_american.getSelectedItem().toString()).equals("Si"))
                selected=true;
            else if((cb_american.getSelectedItem().toString()).equals("No"))
                selected=false; 
            boolean americanV= selected;
            int seriesV= Integer.parseInt(tfd_series.getText());
            
            //aqui se inserta el vehiculo
            
            
            Boolean find_series=false;
            Vehicle vehiculo = new Vehicle(nameV, yearV, mileageV, americanV, seriesV);
            VehicleFile fileVehicle=new VehicleFile(file);
            ArrayList<Vehicle> myArrayVehicles=fileVehicle.getVehicleList();
            for (int i = 0; i <myArrayVehicles.size(); i++) {
                Vehicle temp=myArrayVehicles.get(i);
                if(temp.getSeries()==seriesV){
                find_series=true;
                }//end if
      
            }//end for 
            if(find_series==false && seriesV>=0)
                vehicleFile.addEndRecord(vehiculo);
            else{
                JOptionPane.showMessageDialog(null, "La serie ingresada es invalida");
                tfd_series.setText("");
            }//end else
                
            
          
            // aqui se reseta los texfields
            tfd_name.setText("");
            tfd_year.setText("");
            tfd_mileage.setText("");
            tfd_series.setText("");
                
            
            
            
        }catch(IOException ioe){
             System.out.println("");
        }
         
        }catch(NumberFormatException nme){
        JOptionPane.showMessageDialog(null, "Por favor llene todos los espacios en blanco");
        }
        
    }//GEN-LAST:event_btn_insertVehicleActionPerformed

    
    
